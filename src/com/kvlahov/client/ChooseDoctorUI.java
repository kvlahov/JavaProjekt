    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Doctor selection = null;
        if (rbGeneral.isSelected()) {
            selection = (Doctor) doctorList.getSelectedValue();
        } else if (rbSpecialist.isSelected()) {
            selection = (Doctor) specialistList.getSelectedValue();
        }

        if (selection == null) {
            JOptionPane.showMessageDialog(null, "No doctor selected", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            DoctorUI doctorUi = new DoctorUI(selection);
            doctorUi.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

