    private void initList() {
        DefaultListModel generalDoctorModel = new DefaultListModel();
        DefaultListModel specialistModel = new DefaultListModel();
        DoctorController.getAllDoctors().forEach(d -> {
            if (d.getDepartmentID() == 1) {
                generalDoctorModel.addElement(d);
            } else {
                specialistModel.addElement(d);
            }
        });

        doctorList.setModel(generalDoctorModel);
        specialistList.setModel(specialistModel);
    }

    private void initButtons() {
        btnGroup = new ButtonGroup();
        btnGroup.add(rbGeneral);
        btnGroup.add(rbSpecialist);

        rbGeneral.setSelected(true);
        specialistList.setEnabled(false);

    }

    @Override
    public void start() {
        SwingUtilities.invokeLater(() -> {
            ChooseDoctorUI frame = new ChooseDoctorUI();
            frame.setPreferredSize(new Dimension(800, 600));
            frame.setSize(new Dimension(800, 600));
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
        });
    }
